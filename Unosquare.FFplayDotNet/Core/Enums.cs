namespace Unosquare.FFplayDotNet.Core
{
    /// <summary>
    /// Enumerates the different Media Types
    /// </summary>
    public enum MediaType
    {
        /// <summary>
        /// Represents a null media type
        /// </summary>
        None = -1,

        /// <summary>
        /// The video media type (0)
        /// </summary>
        Video = 0,
        
        /// <summary>
        /// The audio media type (1)
        /// </summary>
        Audio = 1,
        
        /// <summary>
        /// The subtitle media type (3)
        /// </summary>
        Subtitle = 3,
    }
}
